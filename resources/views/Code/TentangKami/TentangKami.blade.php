        <li>Implementasi PTKD.</li>
      </ul>
      <h2>Kedudukan, fungsi dan manfaat PTKD:</h2>
      <ul>
        <li>PTKD merupakan sistem informasi yang diterapkan di tingkat desa, dikembangkan oleh Pemerintah Daerah dan terintegrasi melalui sistem informasi yang ada di tingkat Daerah.</li>
        <li>PTKD dikelola oleh Pemerintah Desa secara daring.</li>
        <li>PTKD merupakan satu-satunya sistem informasi yang diterapkan oleh Pemerintah Desa.</li>
        <li>PTKD menjadi sistem pendukung yang mudah dan akurat untuk pengelolaan sumber daya desa secara berkelanjutan.</li>
      </ul>
      <h2>PTKD berfungsi sebagai media:</h2>
      <ul>
        <li>untuk mengelola data desa</li>
        <li>informasi dan komunikasi pemerintahan desa</li>
        <li>pelayanan administrasi dan pengelolaan keuangan desa; dan</li>
        <li>pengelolaan informasi sumber daya dan potensi kawasan perdesaan.</li>
      </ul>
      <h2>PTKD bermanfaat untuk:</h2>
      <ul>
        <li>memudahkan Pemerintah Desa dalam mengakses, menyimpan dan mengolah data desa;</li>
        <li>meningkatkan kualitas pengelolaan data desa yang akurat dan terbarukan secara berkala;</li>
        <li>menjaga kerahasiaan informasi;</li>
        <li>meningkatkan kualitas pelayanan administrasi Pemerintah Desa;</li>
        <li>mempermudah akses informasi tentang desa;</li>
        <li>meningkatkan akuntabilitas dan transparansi Pemerintah Desa;</li>
        <li>menemukan potensi sumberdaya yang bisa dioptimalkan untuk mendukung kemandirian desa;</li>
        <li>meningkatkan kualitas perencanaan dan perumusan kebijakan pembangunan desa dan kawasan perdesaan oleh pemerintah dari tingkat pusat sampai ke tingkat desa;</li>
        <li>mengefektifkan pelaksanaan kebijakan, program dan kegiatan Pemerintah Desa;</li>
        <li>memudahkan pemerintah dalam melakukan pemantauan perkembangan desa secara nyata.</li>
      </ul>

